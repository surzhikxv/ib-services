cue("Три личных вопроса. Каждый сразу привязываешь к делу — тогда это не допрос, а «чтоб система легла под тебя».")
speech("Слушай, до того как лезть в функции — хочу понять тебя и проект чуть глубже. Я не хочу строить абстрактную систему, я хочу строить твою. Поэтому пара вопросов.")
speech("Первое. Ты этот проект через год-два как видишь? Это для тебя личная история про ДЦП, которая выросла в бизнес, или ты целишь в большой продукт, который потом масштабируется и без твоего лица? От этого зависит, что мы вообще в систему закладываем.")
cue("Амбиция + строим под личный бренд или под отчуждаемый продукт.")
speech("Понял. А что сейчас бесит больше всего? Вот по ощущению — где теряешь время или деньги, где идёшь как будто вслепую? Мне это важнее любых метрик. Система в первую очередь должна снять именно эту боль, а не быть красивой панелькой.")
cue("Тут он сам назовёт, на что давить при продаже. Запоминай дословно.")
speech("И последнее из личного. Ты по натуре сам во всё вникаешь, или тебе важнее отдать и получать результат? Спрашиваю не просто так — это решает, делаю я тебе глубокую панель с цифрами, или просто бот раз в неделю говорит человеческим языком «делай вот это».")
