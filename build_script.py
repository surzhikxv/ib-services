cue("Закрываешь скрытый вопрос «а ты потянешь?». Говоришь спокойно, как факт, и сразу дальше — реакции не жди.")
speech("Пару слов про меня, чтоб ты понимал, с кем имеешь дело — коротко, без резюме на десять минут. Инфопродукты продюсирую уже два года. Начинал с продуктов друзей — первый был курс по маникюру у знакомой, с нуля собирал ей воронку, контент, продажи. Звучит несолидно, но именно на нём я руками прощупал всё то, про что мы сегодня говорим: откуда идёт трафик, где люди отваливаются, что реально приносит деньги. Первый же запуск вывели за пару месяцев на несколько сотен учеников.")
speech("Дальше пошли проекты крупнее и в других нишах. Назвать поимённо не могу, почти везде NDA, такая специфика. Но если коротко — запуски уже не на сотни тысяч, а в разы больше. Воронки я вижу не по теории, а потому что сам их сто раз собирал и ломал.")
speech("И вторая половина — я технарь. Несколько лет пишу код: телеграм-боты, автоматизации, аналитика, связки с оплатами и API. Через мои боты прошли десятки тысяч людей. Я к чему — обычно это два разных человека: продюсер, который в код не умеет, и программист, который не шарит в воронках. Со мной тебе не надо их сводить и переводить с одного языка на другой. Я эту систему и придумал, и сам соберу. Поэтому и в бюджет влезаю — мне не надо платить третьим людям.")

