"""Платёжные ссылки воронки — ВСТАВЬТЕ СЮДА, когда будут готовы.

В BotHelp кнопка «Оплата» вела на динамическую ссылку {%payment_N%} (своя для каждого
тарифа). Пока ссылок нет — кнопка «Оплата» работает как заглушка (бот не ломается,
показывает «ссылка скоро будет»). Как только вставите ссылку ниже — кнопка станет
обычной рабочей кнопкой-ссылкой.

Можно задать и через переменные окружения (.env), не трогая код:
    PAY_BASIC_URL=...    PAY_STANDARD_URL=...    PAY_PREMIUM_URL=...
"""
from __future__ import annotations

import os

# Тариф → платёжная ссылка. Пусто = заглушка. Впишите URL прямо здесь ИЛИ через .env.
PAYMENT_LINKS: dict[str, str] = {
    "basic": os.getenv("PAY_BASIC_URL", ""),       # 💚 Базовый — 1699 ₽  (шаг 2)
    "standard": os.getenv("PAY_STANDARD_URL", ""),  # ⭐ Стандарт + — 1990 ₽ (шаг 3)
    "premium": os.getenv("PAY_PREMIUM_URL", ""),    # 👑 Премиум — 2990 ₽   (шаг 4)
}

# Текст заглушки, пока ссылка не задана (нажатие на «Оплата»).
PAYMENT_PLACEHOLDER = "💳 Ссылка на оплату скоро будет добавлена"


def payment_url(tariff: str | None) -> str:
    """Вернуть платёжную ссылку тарифа или пустую строку, если ещё не задана."""
    if not tariff:
        return ""
    return (PAYMENT_LINKS.get(tariff) or "").strip()


# Режим тестирования воронки без реальной оплаты.
# False (по умолчанию): «Оплата» без ссылки = заглушка.
# True: «Оплата» сразу ведёт на страницу «Оплата прошла» (шаги 5/6/8) — чтобы пройти
#       всю воронку целиком до канала, пока платёжка не подключена. Включается так:
#           BOT_SIMULATE_PAYMENT=1 python -m bot.bot
SIMULATE_PAYMENT = os.getenv("BOT_SIMULATE_PAYMENT", "").strip() in {"1", "true", "yes"}
