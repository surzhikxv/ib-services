b("Полностью автоматическая рассылка партнёрам.")
b("Аналитика платной рекламы, когда подключите таргет.")
b("Глубокий мониторинг конкурентов в реальном времени.")

head("Что нужно от вас, чтобы начать")
b("Доступы к аккаунтам: YouTube, VK, Telegram, TikTok, Instagram.")
b("Токен собственного Telegram-бота и права администратора канала.")
b("Какой тип аккаунтов в TikTok и Instagram — от этого зависит, тянем данные автоматом или через выгрузки.")
b("Список конкурентов и кого вы видите подходящим партнёром для коллабов.")

head("По расходам")
para("Сборку оплачиваете один раз. Сервер и ИИ работают постоянно, поэтому идут отдельной строкой каждый месяц — "
     "это уже не сборка, а чужие мощности (хостинг и API). Начнём с минимума, дальше по факту нагрузки.")

