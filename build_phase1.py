# -*- coding: utf-8 -*-
"""Состав работ Phase 1 — пишем от первого лица, простым языком (без AI-штампов)."""
from docx import Document
from docx.shared import Pt, RGBColor, Cm

DARK = RGBColor(0x20, 0x20, 0x20)
HEAD = RGBColor(0x1F, 0x38, 0x64)  # спокойный тёмно-синий, как в обычном ворде

doc = Document()
normal = doc.styles["Normal"]
normal.font.name = "Calibri"
normal.font.size = Pt(11)
normal.font.color.rgb = DARK
normal.paragraph_format.space_after = Pt(6)
normal.paragraph_format.line_spacing = 1.15

for sec in doc.sections:
    sec.top_margin = Cm(2.2)
    sec.bottom_margin = Cm(2.0)
    sec.left_margin = Cm(2.3)
    sec.right_margin = Cm(2.3)


def title(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(4)
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(18)
    r.font.color.rgb = DARK


def head(text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(3)
    r = p.add_run(text)
    r.bold = True
    r.font.size = Pt(13)
    r.font.color.rgb = HEAD


def para(text, italic=False, size=11):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.italic = italic
    r.font.size = Pt(size)


def b(text):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.space_after = Pt(3)
    r = p.add_run(text)
    r.font.size = Pt(11)


# ===================================================================
title("Контур роста проекта — что делаю в первом этапе")
para("Ниже — всё, что входит в первый этап. По этому списку можно собирать договор. "
     "Срок — 3–4 недели с того момента, как получу доступы.")

head("1. Свожу все данные в одно место")
para("Сейчас данные разбросаны по площадкам, Telegram-воронке и оплатам, общей картины нет. Я собираю их в одну базу.")
b("YouTube, VK и Telegram (канал и бот) подключаю по их API — цифры подтягиваются сами.")
b("TikTok и Instagram беру через выгрузки: без бизнес-аккаунтов прямого доступа к данным там нет, поэтому загружаю экспортом.")
b("Собственный Telegram-бот записывает запуски, переходы по шагам и оплаты Prodamus прямо в общую базу.")
b("Данные из разных мест привожу к одному виду, чтобы их можно было складывать вместе и сравнивать.")

head("2. Собираю воронку целиком — от видео до оплаты")
para("Размечаю весь путь человека: посмотрел видео, перешёл в Telegram, запустил бота, прошёл шаги воронки, оплатил.")
b("Откуда пришёл человек, беру из размеченных старт-ссылок Telegram-бота. Если разметки не хватает — добавляю метки для каждого канала.")
b("На выходе видно цепочку по деньгам: вот это видео дало столько-то запусков бота и столько-то оплат на такую-то сумму.")
b("Сейчас такой картины у вас нет: каждый кусок живёт сам по себе, и непонятно, что реально приносит продажи.")

head("3. Делаю дашборд — одну панель с цифрами")
para("Собираю панель на Metabase поверх базы. На ней видно:")
b("воронку по этапам и где сколько людей отваливается;")
b("выручку по тарифам и по источникам трафика;")
b("как всё меняется во времени.")

head("4. Сажаю сверху ИИ-аналитика")
para("Раз в неделю он готовит разбор простым языком: что выросло, что просело, на что обратить внимание, "
     "что стоит попробовать дальше. Ему можно задать вопрос своими словами — например, «почему на прошлой "
     "неделе продаж меньше» — и он ответит по вашим же данным. Разборы и ответы приходят в Telegram и лежат в дашборде.")

head("5. Ищу партнёров для коллабораций")
para("Раз в период собираю список блогеров и реабилитологов, с кем есть смысл сделать совместный контент. "
     "К каждому прикладываю короткий черновик сообщения, чтобы можно было сразу написать. "
     "Критерии подбора задаёте вы.")

head("6. Слежу за нишей")
para("Веду список ваших конкурентов (5–10 по вашему перечню). ИИ время от времени разбирает, что они делают, "
     "и показывает, где вы отстаёте и что можно перенять.")

head("7. Связываю всё вместе и запускаю")
para("Всё это соединяю через n8n: расписания, обновление данных, передача между блоками. Разворачиваю на сервере, "
     "настраиваю резервное копирование. В конце отдаю доступы, короткую инструкцию и на созвоне показываю, как пользоваться.")

