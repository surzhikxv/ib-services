"""Промпты ИИ-аналитика. Чистые строки/сборка — без сети."""
from __future__ import annotations

import json

SYSTEM_PROMPT = (
    "Ты — аналитик инфобизнеса. Пишешь разбор для владельца проекта (курсы "
    "реабилитации детей с ДЦП): трафик из соцсетей ведёт в Telegram-бота, продажи "
    "идут через три тарифа (премиум/базовый/стандарт).\n\n"
    "Правила:\n"
    "- Говори простым языком, по делу, без воды и канцелярита.\n"
    "- Опирайся только на приложенные цифры. Не выдумывай данных; если данных не "
    "хватает для вывода — так и скажи.\n"
    "- Сначала вывод, потом обоснование. Никаких вступлений вроде «В этом отчёте».\n"
    "- Рекомендации — конкретные и выполнимые, привязанные к цифрам.\n"
    "- Если выручка в рублях нулевая — это потому что ещё не заданы цены тарифов или "
    "не подключены суммы оплат; считай продажи в штуках и отметь это."
)


def _format_digest(digest: dict) -> str:
    return json.dumps(digest, ensure_ascii=False, indent=2)


def build_report_prompt(digest: dict) -> str:
    """Промпт для еженедельного разбора."""
    return (
        "Вот срез данных проекта на текущий момент:\n\n"
        f"{_format_digest(digest)}\n\n"
        "Сделай короткий разбор простым языком:\n"
        "1) Что выросло и что просело (по неделям, если рядов хватает).\n"
        "2) На что обратить внимание прямо сейчас (узкие места воронки, тарифы, источники).\n"
        "3) Что стоит попробовать дальше — 2–3 конкретных шага.\n"
        "Будь краток: владелец читает это между делами."
    )


def build_question_prompt(digest: dict, question: str) -> str:
    """Промпт для ответа на вопрос владельца своими словами."""
    return (
        "Вопрос владельца:\n"
        f"{question}\n\n"
        "Ответь по данным ниже. Если данных недостаточно для точного ответа — скажи, "
        "чего не хватает.\n\n"
        f"Данные проекта:\n{_format_digest(digest)}"
    )
